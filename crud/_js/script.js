function load_modal_cad(nome, sobrenome, idade) {
    $('#nome').val(nome);
    $('#sobrenome').val(sobrenome);
    $('#idade').val(idade);
}

function load_modal_alt(nome, sobrenome, idade, id) {
    $('#alt_nome').val(nome);
    $('#alt_sobrenome').val(sobrenome);
    $('#alt_idade').val(idade);
    $('#alt_id').val(id);
}